package conditionalStatements;

public class forDemo {
	public static void main(String[] args) {
		int[] arr= {1,2,3,4,5};
		
		//Conventional For Loop in JAVA
		for( int i=1;i<5;i++) 
			System.out.println(arr[i]);
		//For Each loop In JAVA
		for(int X:arr) {
			System.out.println(X);
			
		}
		}

}
